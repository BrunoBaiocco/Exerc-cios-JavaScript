// Faça uma função chamada 50PctNumber que recebe um parâmetro chamado val 
// do tipo number e retorna um number equivalente a 50% do valor do parâmetro;

function PctNumber50(val) {

    // Retorna um number onde para obter a porcentagem, ele multiplica por 50
    // e divide o valor total que é 100, ou seja essa expressão obtem 50% do valor do parâmetro;
    return val * 50 / 100;
}

