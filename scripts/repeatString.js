// Faça uma função chamada repeatString que recebe um parâmetro chamado text do tipo string
// e uma chamada n do tipo number e retorna uma string que é o parâmetro text repetido n vezes;

function repeatString(text, n) {
    
    // Está retornando a função text com a o método repeat e assim ele retorna uma nova string e a 
    // chamada n indica o determinado número de cópias concatenadas da string original;
	return (text.repeat(n));
}


