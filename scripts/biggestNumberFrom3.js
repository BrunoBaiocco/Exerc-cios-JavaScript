// Faça uma função chamada biggestNumberFrom3 que recebe três parâmetros chamados
// val1, val2 e val3 do tipo number e retorna um number que é o parâmetro de maior valor;


function biggestNumberFrom3(val1, val2, val3) {
    // Está retornando os três parâmetros, está sendo ultilizado um operador
    // de conjunção que é o &&, este operador está retornando o maior valor;
    return val1 && val2 && val3;
}

