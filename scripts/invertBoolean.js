// Faça uma função chamada invertBoolean que recebe um parâmetro
// chamado val do tipo boolean e retorna o boolean invertido;

function invertBoolean(val) {

    // Está retornando uma verificação com o operador not, que retorna valores não iguais na expressão;
    return val != true;
}

