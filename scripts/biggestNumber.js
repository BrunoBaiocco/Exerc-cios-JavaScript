// Faça uma função chamada biggestNumber que recebe dois parâmetros chamados
// val1 e val2 do tipo number e retorna um number que é o parâmetro de maior valor;

function biggestNumber(val1, val2) {
    // Está retornando os dois parâmetros, está sendo ultilizado um operador
    // de conjunção que é o &&, este operador retorna o maior valor;
    return val1 && val2;
}

