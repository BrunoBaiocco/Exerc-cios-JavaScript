// Faça uma função chamada increaseNumberBy10Pct que recebe um parâmetro chamado val 
// do tipo number e retorna um number equivalente ao valor do parâmetro + 10%;


function increaseNumberBy10Pct(val) {

    // Retorna um number com o valor do parâmetro, sabendo que 1
    //  é igual a 100% do valor, ele soma 10% e adiciona ao valor total;
    return val * 1.1;
}

